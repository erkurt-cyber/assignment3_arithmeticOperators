package assignment3_arithmeticOperators;

public class Question4 {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 20;
		int swap = 0;
		
		System.out.println("num1 = "+num1);
		System.out.println("num2 = "+num2);
		
		System.out.println();
		System.out.println("-------------");
		System.out.println();
		
		swap = num1;
		num1 = num2;
		num2 = swap;
		
		System.out.println("num1 = "+num1);
		System.out.println("num2 = "+num2);
		
		
		
	}

}
